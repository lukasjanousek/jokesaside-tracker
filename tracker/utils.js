function calculateCredit(paymentCzk, tiers = null) {
  if (!tiers) return paymentCzk;
  let remaining = paymentCzk;
  let totalCredit = 0;
  for (const tier of tiers) {
    if (remaining <= 0) break;
    const tierWidth = tier.to_czk - tier.from_czk;
    const amountInTier = Math.min(remaining, tierWidth);
    const creditInTier = amountInTier / (1 - tier.discount_pct);
    totalCredit += creditInTier;
    remaining -= amountInTier;
  }
  return totalCredit;
}

